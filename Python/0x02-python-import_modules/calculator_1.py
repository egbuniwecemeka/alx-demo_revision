#!/usr/bin/python3

def add(a, b):
    """
        Function: Returns the result of the addition of two integers

        Args:
            a: First integer
            b: Second integer

        Return: The sum of the two integers
    """
    return (a + b)

def sub(a, b):
    """
        Function: Returns the result of the subtraction of two integers

        Args:
            a: First integer
            b: Second integer

        Return: The difference of the two integers
    """
    return (a - b)

def mul(a, b):
    """
        Function: Returns the result of the multiplication of two integers

        Args:
            a: First integer
            b: Second integer

        Return: The product of the two integers
    """
    return (a * b)

def div(a, b):
    """
        Function: Returns the result of the division of two integers

        Args:
            a: First integer
            b: Second integer

        Return: The quoient of the two integers
    """
    return (a / b)
